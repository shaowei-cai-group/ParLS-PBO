void PRS::arg_parse(int argc, char **argv) {
    for (int i = 1; i < argc; i++) {
        char *arg = argv[i];
        if (arg[0] != '-' || arg[1] != '-') {
            filename = arg; continue;
        }
        int l = strlen(arg), pos = 0;
        for (int i = 2; i < l; i++)
            if (arg[i] == '=') pos = i;
        if (!pos) continue;
        char name[50];
        strncpy(name, arg + 2, pos - 2);
        name[pos - 2] = '\0';
        char* val = arg + pos + 1;
        if (opt->map_int.count(name)) opt->set_para(name, atoi(val));
        else if (opt->map_double.count(name)) opt->set_para(name, atof(val));
    }
    opt->sync_paras();
    opt->print_change();
    opt_result = opt->identify_opt(filename);
	printf("c opt result: %lld\n", opt_result);
}

char *read_whitespace(char *p) {
    while ((*p >= 9 && *p <= 13) || *p == 32)
        ++p;
    return p;
}
char *read_until_new_line(char *p)
{
    while (*p != '\n')
    {
        if (*p == '\0')
            exit(0);
        ++p;
    }
    return ++p;
}

char *read_int(char *p, ll *i)
{
    *i = 0;
    bool sym = true;
    while ((*p != '-') && (*p < '0' || *p > '9')) p++;
    if (*p == '-')
        sym = false, ++p;
    while (*p >= '0' && *p <= '9')
    {
        if (*p == '\0')
            return p;
        *i = *i * 10ll + *p - '0';
        ++p;
    }
    if (!sym)
        *i = -(*i);
    return p;
}

void PRS::opb2wecnf(char *filename) {
    ifstream fin(filename);
    fin.seekg(0, fin.end);
    size_t file_len = fin.tellg();
    fin.seekg(0, fin.beg);
    char *data = new char[file_len + 1];
    fin.read(data, file_len);
    fin.close();
    data[file_len] = '\0';
    char *p = data;
    clause.push();
    int line_id = 0, num_clauses = 0;
    while (*p != '\0') {
        ++line_id;
        p = read_whitespace(p);
        if (*p == '\0') break;
        if (*p == '*' && line_id == 1) {
            ll v = 0, c = 0;
            p = read_int(p, &v);
            p = read_int(p, &c);
            vars = (int) v, clauses = (int) c;
        }
        else if (*p == '*')
            p = read_until_new_line(p);
        else if (*p == 'm' && *(p + 1) == 'i' && *(p + 2) == 'n') {
            p = read_whitespace(p);
            while (*p != ';') {
                ll lit1, lit2;
                p = read_int(p, &lit1);
                p = read_int(p, &lit2);
                clause[num_clauses].push(lit1);
                clause[num_clauses].push(lit2);
                p = read_whitespace(p);
            }
            clause.push(), num_clauses += 1;
            p = read_until_new_line(p);
        }
        else if (*p == '>') {
            assert(*(p + 1) == '=');
            ll lit;
            p = read_int(p, &lit);
            clause[num_clauses].push(lit);
            clause.push(), num_clauses += 1;
            p = read_until_new_line(p);
        }
        else if (*p == '=') {
            ll lit, coeff_sum = 0;
            p = read_int(p, &lit);
            clause[num_clauses].push(lit);
            clause.push(), num_clauses += 1;
            for (int i = 0; i < clause[num_clauses - 1].size() - 1; i += 2) {
                clause[num_clauses].push(clause[num_clauses - 1][i]);
                clause[num_clauses].push(-clause[num_clauses - 1][i + 1]);
                coeff_sum += clause[num_clauses - 1][i];
            }
            clause[num_clauses].push(coeff_sum - 1);
            clause.push(), num_clauses += 1;
            p = read_until_new_line(p);
        }
        else {
            ll lit1, lit2;
            p = read_int(p, &lit1);
            p = read_int(p, &lit2);
            clause[num_clauses].push(lit1);
            clause[num_clauses].push(lit2);
        }
    }
    // if (num_clauses != clauses + 1)
    //     printf("c %d %d\n", num_clauses, clauses + 1);
    // assert(num_clauses == clauses + 1);
    ll top_clause_weight = 1;
    int l = clause[0].size(); // clause[0]: obj function
    for (int i = 0; i < l; i += 2)
        top_clause_weight += clause[0][i] > 0 ? clause[0][i] : -clause[0][i];
    
    printf("p wcnf %d %d %lld\n", vars, num_clauses - 1 + (l >> 1), top_clause_weight);

    for (int i = 1; i < num_clauses; i++) {
        int len = clause[i].size() - 1;
        ll &constrain_value = clause[i][len];
        for (int j = 0; j < len; j += 2) {
            ll &coeff = clause[i][j], &var = clause[i][j + 1];
            if (coeff < 0) {
                coeff = -coeff, var = -var;
                constrain_value += coeff;
            }
        }
        printf("%lld %lld ", top_clause_weight, constrain_value);
        for (int j = 0; j < len; j++)
            printf("%lld ", clause[i][j]);
        puts("0");
    }

    for (int i = 0; i < l; i += 2) {
        ll &coeff = clause[0][i], &var = clause[0][i + 1];
        if (coeff < 0) coeff = -coeff;
        else var = -var;
        printf("%lld 1 1 %lld 0\n", coeff, var);
    }
}

